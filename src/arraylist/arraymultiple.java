package arraylist;

public class arraymultiple {

	public static void main(String[] args) {

		int a[][]= new int[2][3];
		a[0][0]=6;
		a[0][1]=5;
		a[0][2]=4;
		a[1][0]=3;
		a[1][1]=2;
		a[1][2]=1;
		for(int i=0; i<1; i++) {
			for(int j=0; j<=2; j++ ) {
		
		System.out.println(a[i][j]);
		
	}
			System.out.println();

		}
}}
