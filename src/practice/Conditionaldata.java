package practice;

public class Conditionaldata {

	public static void main(String[] args) {

	int usd=162, rupees=2;
	
	System.out.println(usd>rupees  && usd==rupees); //false
	
	System.out.println(usd<rupees  && usd==rupees); //false
	
	System.out.println(usd<rupees  || usd>rupees); //true
		
		
		
	}

}
