package whilee;

public class Switch1 {

	public static void main(String[] args) {

	String state = "ttyyu";
	switch(state) {
	case "Ts":
		System.out.println("Hyderabad");
		break;
	case "Ap":
		System.out.println("Amaravathi");
		break;
	case "Mh":
		System.out.println("Mumbai");
		break;
	case "up":
		System.out.println("Lucknow");
		break;
	case "karnataka":
		System.out.println("Bengluru");
		break;
		default:
			System.out.println("Unable to find the state");
			break;
	}
	
		
		
		
		
	}

}
